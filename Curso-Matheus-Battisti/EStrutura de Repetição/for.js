/*

Aqui temos uma boa demostração de for com else e if alinhado , usando uma estrutura de repetição para ver qual
number e divisel por 5 e 2 



*/

var limete = 100 ;


 for(let i = 0 ; i <= limete ; i++){

  if(i % 5 == 0  && i % 2 == 0 ){ console.log("Ele e ambos " + i)}
else if(i % 2 == 0 ){ console.log("PAR " + i)}
else if(i % 5 == 0 ){ console.log("impar " + i)}

 }
 





